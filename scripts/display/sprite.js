class Sprite {
    constructor(spriteSheet, xSize, ySize, xSprite, ySprite, xFrames, yFrames) {
        this.spriteSheet = spriteSheet;
        this.xSize = xSize;
        this.ySize = ySize;
        this.xSprite = xSprite;
        this.ySprite = ySprite;
        this.xFrames = xFrames;
        this.yFrames = yFrames;
    }
}